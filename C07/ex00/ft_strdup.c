#include <stdlib.h>

char	*ft_strdup(char *src)
{
	char	*new_str;
	int		i;

	i = 0;
	new_str = malloc(sizeof(src));
	if (new_str == NULL)
		return (NULL);
	while (src[i] != '\0')
	{
		new_str[i] = src[i];
		i++;
	}
	new_str[i] = '\0';
	return (new_str);
}
